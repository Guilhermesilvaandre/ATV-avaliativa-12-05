<h1>Os 8 tipo de Surdez</h1>
<h4><p ><strong>1 - Perda auditiva relacionada à idade</strong></p>  
<p>Com o passar dos anos, muitas pessoas experimentam uma diminuição na capacidade auditiva. 
    Essa perda ocorre geralmente em ambas as orelhas e pode começar a partir dos 30 ou 40 anos,
     piorando progressivamente. Sons de alta frequência, como a fala, tornam-se mais difíceis de ouvir, 
     especialmente em ambientes barulhentos. Muitas vezes, o indivíduo não percebe essa mudança, apenas
      ajusta comportamentos, como aumentar o volume da TV ou falar mais alto. As causas são variadas e 
      incluem fatores genéticos, ambientais e de estilo de vida. Essa condição está frequentemente associada
       a alterações no ouvido interno, mas pode também envolver nervos auditivos e estruturas do ouvido médio.</p>

<p><strong>2 - Perda auditiva induzida por ruídos (PAIR)</strong></p>  
<p>Diariamente somos expostos a inúmeros sons, desde o tráfego urbano até música em casa.
     Em níveis seguros, esses ruídos não causam danos, mas quando a exposição é prolongada ou intensa, 
     pode ocorrer perda auditiva induzida por ruído. Essa condição pode surgir de um evento único, como 
     uma explosão, ou da exposição contínua a altos volumes, como o maquinário industrial. Sons superiores 
     a 85 decibéis aumentam significativamente o risco. A perda pode ser imediata ou levar anos para ser
      notada, podendo ser temporária ou permanente, afetando um ou ambos os ouvidos.</p>

<p><strong>3 - Perda auditiva causada por infecções</strong></p>  
<p>Diversos fatores podem influenciar a perda auditiva, como envelhecimento, lesões, ruídos
     excessivos e infecções. Infecções no ouvido médio podem levar ao acúmulo de fluido, dificultando 
     a movimentação do tímpano e dos ossículos auditivos. Isso impede a transmissão adequada dos sons, 
     caracterizando uma perda auditiva condutiva. Esse tipo de perda, diferentemente da neurossensorial, 
     afeta a transmissão sonora antes de alcançar o nervo auditivo. Felizmente, a perda auditiva causada 
     por infecções costuma ser temporária e pode ser resolvida com o tratamento adequado, como antibióticos
      ou tubos de drenagem no tímpano.</p>

<p><strong>4 - Perda auditiva causada por alterações na tireoide</strong></p>  
<p>Alterações na função da tireoide, tanto o hipertireoidismo quanto o hipotireoidismo, podem 
    impactar a audição. Algumas condições relacionadas incluem a síndrome de Pendred, que provoca 
    perda auditiva precoce e o desenvolvimento de bócio; a doença de Graves, associada ao hipertireoidismo;
     e a doença de Hashimoto, um dos principais causadores do hipotireoidismo. Além disso, tratamentos 
     para câncer de tireoide envolvendo radiação podem prejudicar a audição. Para minimizar riscos, é essencial 
     monitorar a saúde auditiva ao longo do tratamento médico.</p>

