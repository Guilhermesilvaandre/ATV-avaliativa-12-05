<p><strong>5 - Perda auditiva relacionada a medicamentos</strong></p>  
<p>Alguns medicamentos podem afetar a audição, causando desde uma perda temporária até danos 
    permanentes. Embora esse efeito colateral não seja amplamente divulgado, ele pode ser significativo
     para algumas pessoas. A perda auditiva induzida por medicamentos pode ocorrer rapidamente e variar em intensidade.</p>  

<p>Entre os medicamentos que podem impactar a audição estão:</p>  
<p>- Aspirina: Em doses elevadas (8 a 12 comprimidos por dia), pode causar perda auditiva temporária.</p>  
<p>- Anti-inflamatórios não esteroides (como ibuprofeno e naproxeno): Associados a problemas auditivos.</p>  
<p>- Antibióticos aminoglicosídeos: Podem levar a uma perda auditiva permanente em até 60% dos casos.</p>  
<p>- Medicamentos quimioterápicos (como cisplatina e carboplatina): Relacionados à perda auditiva significativa.</p>  

<p><strong>6 - Perda auditiva causada por perfuração de tímpano</strong></p>  
<p>A perfuração do tímpano ocorre quando há um rasgo na membrana que separa o canal 
    auditivo do ouvido médio. Isso pode resultar em perda auditiva e aumentar o risco 
    de infecções. Em muitos casos, a membrana timpânica se regenera sozinha, mas algumas situações exigem intervenção médica.</p>  

<p>Sintomas comuns incluem:</p>  
<p>- Dor de ouvido que pode desaparecer rapidamente.</p>  
<p>- Secreção do ouvido (clara, com pus ou sangue).</p>  
<p>- Perda auditiva.</p>  
<p>- Zumbido.</p>  
<p>- Sensação de vertigem, náusea ou vômito.</p>  

<p><strong>7 - Surdez congênita</strong></p>  
<p>A surdez congênita ocorre em aproximadamente 1 a cada 1.000 nascimentos e pode ter 
    origem genética ou não hereditária. A forma mais comum é a herança autossômica recessiva,
     responsável por mais de 75% dos casos.</p>  

<p>Entre as causas genéticas, destaca-se a mutação no gene da conexina, que afeta a
     comunicação celular no ouvido interno. Essa mutação pode ser responsável por até 80% dos casos de surdez não sindrômica em crianças.</p>  

<p><strong>8 - Perda auditiva transitória</strong></p>  
<p>Nem toda perda auditiva é permanente. Algumas condições podem causar uma perda
     temporária, que pode ser revertida com tratamento adequado. Entre as causas 
     mais comuns estão:</p>  
<p>- Exposição a ruídos altos.</p>  
<p>- Infecções de ouvido.</p>  
<p>- Acúmulo de cera no canal auditivo.</p>  

<p>Se a exposição ao ruído for ocasional, a audição pode se recuperar. No entanto,
     a exposição contínua a sons intensos pode levar a danos permanentes. O uso de 
     proteção auditiva é essencial para evitar problemas futuros.</p>
</h4>


